public class Objeto {
    private final String nome;
    private final String dica;

    public Objeto(String nome, String dica) {
        this.nome = nome;
        this.dica = dica;
    }

    public String getNome() {
        return nome;
    }

    public String getDica() {
        return dica;
    }
}
